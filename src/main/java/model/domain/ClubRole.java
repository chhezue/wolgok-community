package model.domain;

public class ClubRole {
    private int clubId;
    private String role;

    // Getter and Setter methods
    public int getClubId() {
        return clubId;
    }

    public void setClubId(int clubId) {
        this.clubId = clubId;
    }

    public String getRole() {
        return role;
    }

    public void setRole(String role) {
        this.role = role;
    }
}